
/* O objeto window abre um pop-up no navegador. Ele tem vários métodos. Abaixo, estão alguns:
*/ 

window.alert('Hello, world!') // Aviso

window.confirm('Gostaria de visualizar esta página?') // Pedido de confirmação

window.prompt('Insira seu nome:') // interage com o usuário recebendo um input

// Também é possível utilizar apenas o nome do método (alert, confirm, prompt), sem utilizar "window".