
// o sinal "+" pode ser usado tanto para somar como para concatenar. A soma só ocorre se todos os valores forem do tipo "number". Do contrário, ocorrerá a concatenação.

//Exemplo de soma:

var n1 = 4

var n2 = 5

console.log(n1+n2)

//Exemplo de concatenação:

var nome = 'José '

var sobrenome = 'Ribeiro'

console.log(nome + sobrenome)









